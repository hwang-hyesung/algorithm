WITH G AS(

SELECT EMP_NO, AVG(SCORE) AVS

FROM HR_GRADE

GROUP BY EMP_NO

)

SELECT E.EMP_NO, E.EMP_NAME,

CASE

WHEN G.AVS >= 96 THEN 'S'

WHEN G.AVS >= 90 THEN 'A'

WHEN G.AVS >= 80 THEN 'B'

ELSE 'C' END AS GRADE,

CASE

WHEN G.AVS >= 96 THEN E.SAL*0.2

WHEN G.AVS >= 90 THEN E.SAL*0.15

WHEN G.AVS >= 80  THEN E.SAL*0.1

ELSE 0 END AS BONUS

FROM HR_EMPLOYEES E

JOIN G

ON E.EMP_NO = G.EMP_NO

ORDER BY E.EMP_NO ASC;
